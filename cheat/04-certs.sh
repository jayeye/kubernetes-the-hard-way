### The Kubelet Client Certificates

#### Kubernetes uses a special-purpose authorization mode
#### https://kubernetes.io/docs/admin/authorization/node/
#### called Node Authorizer, that specifically authorizes API requests
#### made by Kubelets.
#### https://kubernetes.io/docs/concepts/overview/components/#kubelet
#### In order to be authorized by the Node Authorizer, Kubelets must
#### use a credential that identifies them as being in the
#### system:nodes group, with a username of system:node:<nodeName>. In
#### this section you will create a certificate for each Kubernetes
#### worker node that meets the Node Authorizer requirements.


for instance in ${KWORKERS[@]}; do
cat > ${instance}-csr.json <<EOF
{
  "CN": "system:node:${instance}",
  "key": {
    "algo": "rsa",
    "size": 2048
  },
  "names": [
    {
      "C": "US",
      "L": "Portland",
      "O": "system:nodes",
      "OU": "Kubernetes The Hard Way",
      "ST": "Oregon"
    }
  ]
}
EOF

EXTERNAL_IP=$(gcloud compute instances describe ${instance} \
  --format 'value(networkInterfaces[0].accessConfigs[0].natIP)')

INTERNAL_IP=$(gcloud compute instances describe ${instance} \
  --format 'value(networkInterfaces[0].networkIP)')

cfssl gencert \
  -ca=ca.pem \
  -ca-key=ca-key.pem \
  -config=ca-config.json \
  -hostname=${instance},${EXTERNAL_IP},${INTERNAL_IP} \
  -profile=kubernetes \
  ${instance}-csr.json | cfssljson -bare ${instance}
done

